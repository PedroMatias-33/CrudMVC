private void carregarTabela() {
    DefaultTableModel modelo = (DefaultTableModel) Tabela.getModel();
    modelo.setRowCount(0);

    String sql = "SELECT * FROM chamado_tecnico ORDER BY id";

    try (Connection con = ConnectionFactory.getConnection();
         PreparedStatement pst = con.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {

        while (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("solicitante"),
                rs.getString("patrimonio"),
                rs.getString("local_sala"),
                rs.getString("prioridade"),
                rs.getString("problema"),
                rs.getString("diagnostico"),
                rs.getString("status")
            });
        }

    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar tabela: " + e.getMessage());
    }
}
    private void SalvarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarBtnActionPerformed
    try {
        String sql = "INSERT INTO chamado_tecnico "
                + "(solicitante, patrimonio, local_sala, data_chamado, prioridade, problema, diagnostico, status) "
                + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";

        Connection con = ConnectionFactory.getConnection();
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, txtSolicitante.getText().trim());
        pst.setString(2, txtPatrimonio.getText().trim());
        pst.setString(3, txtLocal.getText().trim());
        pst.setString(4, txtData.getText().trim());
        pst.setString(5, cbPrioridade.getSelectedItem().toString());
        pst.setString(6, txtProblema.getText().trim());
        pst.setString(7, txtDiagnostico.getText().trim());
        pst.setString(8, cbStatus.getSelectedItem().toString());

        pst.executeUpdate();

JOptionPane.showMessageDialog(this, "Chamado salvo com sucesso!");

        carregarTabela();

        pst.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao salvar chamado: " + e.getMessage());
    }
    }//GEN-LAST:event_SalvarBtnActionPerformed

    private void EditarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarBtnActionPerformed
    int linha = Tabela.getSelectedRow();

    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um chamado na tabela para editar!");
        return;
    }

    try {
        int id = Integer.parseInt(Tabela.getValueAt(linha, 0).toString());

        String sql = "UPDATE chamado_tecnico SET "
                + "solicitante = ?, "
                + "patrimonio = ?, "
                + "local_sala = ?, "
                + "data_chamado = ?, "
                + "prioridade = ?, "
                + "problema = ?, "
                + "diagnostico = ?, "
                + "status = ? "
                + "WHERE id = ?";

        Connection con = ConnectionFactory.getConnection();
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, txtSolicitante.getText().trim());
        pst.setString(2, txtPatrimonio.getText().trim());
        pst.setString(3, txtLocal.getText().trim());
        pst.setString(4, txtData.getText().trim());
        pst.setString(5, cbPrioridade.getSelectedItem().toString());
        pst.setString(6, txtProblema.getText().trim());
        pst.setString(7, txtDiagnostico.getText().trim());
        pst.setString(8, cbStatus.getSelectedItem().toString());
        pst.setInt(9, id);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Chamado editado com sucesso!");

        carregarTabela();

        pst.close();
        con.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao editar chamado: " + e.getMessage());
    }
    }//GEN-LAST:event_EditarBtnActionPerformed

