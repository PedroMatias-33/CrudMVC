    private void ExcluirBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirBtnActionPerformed
    int linha = Tabela.getSelectedRow();

    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Selecione um chamado na tabela para excluir!");
        return;
    }

    int resposta = JOptionPane.showConfirmDialog(
            this,
            "Deseja realmente excluir este chamado?",
            "Confirmação",
            JOptionPane.YES_NO_OPTION
    );

    if (resposta == JOptionPane.YES_OPTION) {
        try {
            int id = Integer.parseInt(Tabela.getValueAt(linha, 0).toString());

            String sql = "DELETE FROM chamado_tecnico WHERE id = ?";

            Connection con = ConnectionFactory.getConnection();
            PreparedStatement pst = con.prepareStatement(sql);

            pst.setInt(1, id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Chamado excluído com sucesso!");

            carregarTabela();

            pst.close();
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir chamado: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_ExcluirBtnActionPerformed

